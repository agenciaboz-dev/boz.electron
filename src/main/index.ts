// code. You can also put them in separate files and require them here.

app.setLoginItemSettings({
  openAtLogin: true
})

let isSingleInstance = app.requestSingleInstanceLock()
if (!isSingleInstance) {
  app.quit()
}

app.setAsDefaultProtocolClient('bozapp')